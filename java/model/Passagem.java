package model;

public class Passagem {
	private int ID_Passagem;
	private int ID_voo;
	private String classe;
	private int num_Assento;
	
	
	public int getID_Passagem() {
		return ID_Passagem;
	}
	public void setID_Passagem(int iD_Passagem) {
		ID_Passagem = iD_Passagem;
	}
	public int getID_voo() {
		return ID_voo;
	}
	public void setID_voo(int iD_voo) {
		ID_voo = iD_voo;
	}
	public String getClasse() {
		return classe;
	}
	public void setClasse(String classe) {
		this.classe = classe;
	}
	public int getNum_Assento() {
		return num_Assento;
	}
	public void setNum_Assento(int num_Assento) {
		this.num_Assento = num_Assento;
	}
	
	
	
	

}
